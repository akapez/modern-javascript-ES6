//switch statements

const grade = 'A'

switch (grade) {
  case 'A':
    console.log('your got an A')
    break
  case 'B':
    console.log('your got a B')
    break
  case 'C':
    console.log('your got a C')
    break
  case 'D':
    console.log('your got a D')
    break
  default:
      console.log("Invalid marks");
    break
}
